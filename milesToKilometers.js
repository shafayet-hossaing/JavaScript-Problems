
// Miles To Kilometers
function milesToKilometers(miles) {
    let kilometers = miles * 1.60934; // Main Calculation == Multiplying Miles By 1.60934
    return kilometers; // Return Kilometers Variable For Putting The Function In A New Variable
}
let givenMiles = milesToKilometers(50) // Calling The Function With Argument
console.log(givenMiles);
